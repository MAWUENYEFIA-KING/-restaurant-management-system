    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String answer = TEXT_ANSWER.getText();
        String newPassword = TEXT_NEW_PASSWORD.getText();
        if (answer.equals(dbAnswer)) {
            UserDoc.update(email, newPassword);
            Clear();
        } else {
            JOptionPane.showMessageDialog(null, "<html><b style=\"color:red\">Incorrect Answer</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        email = TEXT_EMAIL.getText();
        User user = null;
        user = UserDoc.getSecurityQuestion(email);
        if (user == null) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"color:red\">Incorrect Email</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else {
            btnSearch.setEnabled(false);
            TEXT_EMAIL.setEditable(false);
            dbAnswer = user.getAnswer();
            TEXT_SECURITY_QUESTION.setText(user.getSecurity_question());
            ValidateFields();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

