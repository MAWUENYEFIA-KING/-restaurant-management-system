    public void ValidateEmail() {
        email = TEXT_EMAIL.getText();
        if (email.matches(emailPattern)) {
            btnSearch.setEnabled(true);
        } else {
            btnSearch.setEnabled(false);
        }
    }

    public void ValidateFields() {
        String password = TEXT_NEW_PASSWORD.getText();
        String answer = TEXT_ANSWER.getText();
        String securityQuestion = TEXT_SECURITY_QUESTION.getText();
        if (!password.equals("") && !answer.equals("") && !securityQuestion.equals("")) {
            btnUpdate.setEnabled(true);
        } else {
            btnUpdate.setEnabled(false);
        }
    }

