    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int index = jTable2.getSelectedRow();
        if (index != -1) { // Ensure a row is selected
            int a = JOptionPane.showConfirmDialog(null, "Do you want to remove this product", "Confirm", JOptionPane.YES_NO_OPTION);
            if (a == JOptionPane.YES_OPTION) {
                DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
                String total = model.getValueAt(index, 3).toString();
                double totalAmount = Double.parseDouble(total); // Parse as double
                grandTotal -= totalAmount; // Update grandTotal
                LBLGRANT_TOTAL.setText(String.valueOf(grandTotal));
                model.removeRow(index); // Remove row from table model
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row to remove.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTable2MouseClicked

