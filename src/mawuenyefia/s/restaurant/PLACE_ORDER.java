    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String productName = model.getValueAt(index, 0).toString();
        PRODUCT product = PRODUCT_DOC.getProductByName(productName);
        TEXT_PRO_NAME.setText(product.getName());
        TEXT_PRO_PRICE.setText(String.valueOf(product.getPrice()));
        jSpinner1.setValue(1);
        TEXT_PRO_TOTAL.setText(String.valueOf(product.getPrice()));
        productPrice = Double.parseDouble(product.getPrice());
        productTotal = Double.parseDouble(product.getPrice());
        btnAdd_To_Cart.setEnabled(true);

    }//GEN-LAST:event_jTable1MouseClicked

