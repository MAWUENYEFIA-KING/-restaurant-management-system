    public void ProductNameByCategory(String category) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<PRODUCT> list = PRODUCT_DOC.getAllRecordsByCategory(category);
        Iterator<PRODUCT> itr = list.iterator();
        while (itr.hasNext()) {
            PRODUCT productObj = itr.next();
            dtm.addRow(new Object[]{productObj.getName()});
        }
    }

    public void FilterProductByName(String name, String category) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<PRODUCT> list = PRODUCT_DOC.FilterProductByName(name, category);
        Iterator<PRODUCT> itr = list.iterator();
        while (itr.hasNext()) {
            PRODUCT productObj = itr.next();
            dtm.addRow(new Object[]{productObj.getName()});
        }
    }

