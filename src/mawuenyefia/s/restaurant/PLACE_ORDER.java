    public void ValidateField() {
        String customerName = TEXT_CUS_NAME.getText();
        String customermobileNumber = TEXT_CUS_MOBILE.getText();
        String customerEmail = TEXT_CUS_EMAIL.getText();
        if (!customerEmail.equals("") && customerName.matches(namePattern) && customermobileNumber.matches(mobileNumberPattern) && customermobileNumber.length() == 10 && customerEmail.matches(emailPatttern) && grandTotal > 0) {
            btnGenerateBill.setEnabled(true);
        } else {
            btnGenerateBill.setEnabled(false);
        }
    }

